public class globalVlocal {
    int a=10;
    static int b=9;

    public static void main(String[] args) {
        int a=90;
        b=3;
        int c=90/7;
        float d=a/7f;
        double e=a/7f;
        String s="Soumodip";
        System.out.println(s);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println();
    }
}
